<script>
document.addEventListener("DOMContentLoaded", () => {
    document.querySelectorAll('.animate-counter').forEach(counter => {
        const target = parseFloat(counter.getAttribute('data-target'));
        const suffix = counter.getAttribute('data-suffix') || '';
        
        const totalDuration = 1500; 
        const frameRate = 1000 / 60; 
        const totalFrames = Math.round(totalDuration / frameRate);
        
        let currentFrame = 0;

        const animate = () => {
            currentFrame++;
            
            const progress = currentFrame / totalFrames;
            const easeOutProgress = 1 - Math.pow(1 - progress, 3); 
            let currentValue = target * easeOutProgress;

            if (currentFrame < totalFrames) {
                counter.innerText = (target % 1 !== 0 ? currentValue.toFixed(1) : Math.floor(currentValue)) + suffix;
                requestAnimationFrame(animate);
            } else {
                counter.innerText = target + suffix;
            }
        };

        animate();
    });
});
</script>